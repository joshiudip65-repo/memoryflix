export function getRailsByType(type: string): Rail[] {
  return mockRails.filter((r) => r.type === type && r.isActive);
}

export function getActiveRails(): Rail[] {
  return mockRails.filter((r) => r.isActive).sort((a, b) => a.position - b.position);
}

export function getActiveBanners(): HeroBanner[] {
  return mockBanners.filter((b) => b.isActive).sort((a, b) => a.position - b.position);
}
